# Arrays oder Sequenzen 
# Arrays sind, Tupel, List und Dictionary 

# Tupel sind eine Sammlung von werten, die nicht manipuliert werden können

tuple_Primzahlen = (1, 3, 5, 7, 11, 13, 17, 19, 23, 28)
# List ist eine Sammlung von werten die manipuliert werden darf
list_Gewichte = [2.5, 5, 7.5, 10, 12.5, 15, 17.5, 20]

# Dictionarys sind eine Sammlung von 
dict_Adresse = {"Name":"Nachname1", "Vorname":"Vorname1", "PLZ":"1000", "Ort":"Ort1", "Straße":"Straße1"}

print(tuple_Primzahlen[7], "\n")    # Zeigt die 8. Stelle des tuples an
print(list_Gewichte[3], "\n")       # Zeigt die 4. Stelle der lise an 
print(dict_Adresse["PLZ"], "\n")    # Zeigt den Wert der ausgewählten variable an 

while True:
    user_choice = input("Enter your choice (1 or 2): ")

    if user_choice == "1":
        print("You chose option 1.")
        break  # Exit the loop when the correct choice is made
    elif user_choice == "2":
        print("You chose option 2.")
        break  # Exit the loop when the correct choice is made
    else:
        print("Invalid choice. Please enter 1 or 2. Try again.")
